print("Enter two integers, and I will tell you""the relationships they satisfy.")

number1 = int(input('enter first number: '))
number2 = int(input('enter second number: '))
if number1 == number2:
    print(number1, 'is equals to', number2)
if number1 != number2:
    print(number1, 'is not equals to', number2)
if number1 > number2:
    print(number1, 'is greater than', number2)
if number1 < number2:
    print(number1, 'is less than', number2)
if number1 >= number2:
    print(number1, 'is greater or equals', number2)
if number1 <= number2:
    print(number1, 'is less than or equals', number2)





